def validate_portfolio(portfolio: Portfolio) -> None:
    required = [
        "site/index.html",
        "site/styles.css",
        "site/script.js",
        ".github/workflows/deploy-pages.yml",
        "README.md",
        ".gitignore",
        "site/assets/icons/favicon.png",
    ]
    missing = [item for item in required if not (portfolio.path / item).exists()]
    evidence_missing = [
        filename for filename, _, _ in SITE_EVIDENCE
        if not (portfolio.path / "site" / "assets" / "screenshots" / filename).exists()
    ]
    public = read_text(portfolio.path / "site" / "index.html")
    forbidden = [
        pattern for pattern in FORBIDDEN_PUBLIC_PATTERNS
        if re.search(pattern, public, flags=0)
    ]
    py_compile = "not applicable"
    main_py = portfolio.path / "main.py"
    if main_py.exists():
        try:
            temp_pyc = BATCH_ROOT / "logs" / f"{portfolio.slug}.pyc"
            temp_pyc.parent.mkdir(parents=True, exist_ok=True)
            subprocess.run(
                [
                    "python",
                    "-c",
                    (
                        "import py_compile,sys;"
                        "py_compile.compile(sys.argv[1], cfile=sys.argv[2], doraise=True)"
                    ),
                    str(main_py),
                    str(temp_pyc),
                ],
                cwd=str(portfolio.path),
                check=True,
                stdout=subprocess.PIPE,
                stderr=subprocess.PIPE,
                timeout=8,
            )
            temp_pyc.unlink(missing_ok=True)
            py_compile = "passed"
        except Exception as exc:  # noqa: BLE001
            temp_pyc.unlink(missing_ok=True)
            py_compile = f"failed: {exc}"
            portfolio.warnings.append(py_compile)
    portfolio.validation = {
        "missing": missing,
        "evidenceMissing": evidence_missing,
        "forbiddenPublicWording": forbidden,
        "favicon": (portfolio.path / "site" / "assets" / "icons" / "favicon.png").exists(),
        "pyCompile": py_compile,
        "valid": not missing and not evidence_missing and not forbidden and not str(py_compile).startswith("failed"),
    }



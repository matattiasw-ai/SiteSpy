def wrap_text(text: str, width: int) -> str:
    words = text.split()
    lines: list[str] = []
    current: list[str] = []
    for word in words:
        trial = " ".join(current + [word])
        if len(trial) > width and current:
            lines.append(" ".join(current))
            current = [word]
        else:
            current.append(word)
    if current:
        lines.append(" ".join(current))
    return "\n".join(lines)


def copy_certificates(portfolio: Portfolio, cert_dir: Path) -> list[dict[str, str]]:
    cert_dir.mkdir(parents=True, exist_ok=True)
    certificates: list[dict[str, str]] = []
    candidates: list[Path] = []
    for name in ["Certificates", "certificates", "Certificate", "certificate"]:
        folder = portfolio.path / name
        if folder.exists():
            candidates.extend(folder.glob("*"))
    for source in candidates:
        if source.is_file() and source.suffix.lower() in {".pdf", ".png", ".jpg", ".jpeg"}:
            dest = cert_dir / source.name
            if source.resolve() != dest.resolve():
                shutil.copy2(source, dest)
            certificates.append({
                "title": source.stem.replace("_", " ").replace("-", " ").title(),
                "file": f"assets/certificates/{source.name}",
            })
    return certificates



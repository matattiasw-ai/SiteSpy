def make_icon(path: Path, portfolio: Portfolio, size: int = 512) -> None:
    theme = site_theme(portfolio)
    path.parent.mkdir(parents=True, exist_ok=True)
    img = Image.new("RGB", (size, size), theme["navy_dark"])
    draw = ImageDraw.Draw(img)
    draw.rounded_rectangle(
        [size * 0.12, size * 0.12, size * 0.88, size * 0.88],
        radius=int(size * 0.12),
        fill=theme["navy"],
        outline=theme["accent"],
        width=max(4, size // 32),
    )
    initials = "".join(part[0] for part in portfolio.name.split()[:2]).upper() or "SP"
    font = load_font(size // 4)
    bbox = draw.textbbox((0, 0), initials, font=font)
    draw.text(
        ((size - (bbox[2] - bbox[0])) / 2, (size - (bbox[3] - bbox[1])) / 2 - size * 0.03),
        initials,
        fill="white",
        font=font,
    )
    img.save(path)


def load_font(size: int) -> ImageFont.ImageFont:
    for font_name in ["arial.ttf", "segoeui.ttf", "DejaVuSans-Bold.ttf"]:
        try:
            return ImageFont.truetype(font_name, size)
        except OSError:
            pass
    return ImageFont.load_default()


